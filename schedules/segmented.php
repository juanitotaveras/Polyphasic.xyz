<h3>Husband Study</h3>
<div class='sect'>
<p> The first documented attempt to compare one continuous block of sleep (monophasic) versus several short sleeps (polyphasic) was that of Husband (1935)<!-- Husband RW (1935): The comparative value of continuous versus interrupted sleep. <i>J Exp Psychol 18:792-796 -->.
Husband hypothesized, "one might sleep two to three hours, stay awake for a number of hours, and then sleep two or three hours more. The prodedure would, in theory, take advantage of two stretches of deep slumber." 
Husband's subject followed a monophasic schedule for one month, sleeping an interrupted 8 hour block of sleep every night. 
He then began a segmented sleep schedule, in which he slept 3 hours, woke up for three hours, and slept a second three-hour core. 
<br><br>
Various performance, physical, and psychological tests showed no signs of deterioration during or after this one-month experimental period. From these results, Husband concluded that "there are no consistent differences between interrupted and continuous sleep; [however] it is recognized that the use of only one subject makes the results only suggestive" (1935, p. 796)
</p>  
