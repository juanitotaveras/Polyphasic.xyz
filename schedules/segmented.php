<h3>Thomas Wehr</h3>
  <div class='sect'>
<p>
Psychiatrist Thomas Wehr conducted an experiment in the early 1990s in which a group of people was placed in an isolated building where they had total darkness for 14 hours every day for one month.
</p>
<p> By the fourth week of the experiment, the subjects settled into a very distinct sleeping pattern. They slept first for four hours, woke up for one or two hours, then returned to sleep for four more hours.</p>
